#include "libft.h"

int ft_putbnr_fd(int n, int fd)
{
	int	ret;

	ret = 0;
	if (n < 0)
	{
		ret += write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
		ret += ft_putbnrfd(n / 10, fd);
	ret += write(fd, &"0123456789"[n % 10], 1);
	return (ret);
}