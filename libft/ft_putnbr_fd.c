#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
	{
		ft_putbnr_fd(n / 10, fd);
		ft_putbnr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd(n + '0', fd);
}
